SET FOREIGN_KEY_CHECKS=0;
SET SQL_SAFE_UPDATES = 0;

DROP TABLE IF EXISTS users;
CREATE TABLE users (
    _user_id INT(6) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    _name VARCHAR(60) NOT NULL,
    _email VARCHAR(50) NOT NULL,
    _security_question VARCHAR(80) NULL,
    _security_answer VARCHAR(40) NULL,
    _bio TEXT NULL,
    _reg_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    _last_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    _hash TEXT,
    _salt TEXT,
    _last_ip VARCHAR(60) NOT NULL, 
    _latitude FLOAT NOT NULL,
	_longitude FLOAT NOT NULL,
    _status  VARCHAR(5) NOT NULL DEFAULT 'ACT',
    UNIQUE (_email)    
)  ENGINE=INNODB DEFAULT CHARSET=UTF8;

SET FOREIGN_KEY_CHECKS=1;
